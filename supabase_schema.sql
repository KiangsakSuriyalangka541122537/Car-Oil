-- 1. รันคำสั่งนี้เพื่ออัปเดตตารางเริ่มต้นของระบบชื่อ "Car-Oil fuel_entries" (หากคุณใช้ตารางชื่อนี้)
ALTER TABLE "Car-Oil fuel_entries" 
ADD COLUMN IF NOT EXISTS category text DEFAULT 'fuel';

ALTER TABLE "Car-Oil fuel_entries" 
ADD COLUMN IF NOT EXISTS notes text DEFAULT '';


-- 2. รันคำสั่งนี้เพื่ออัปเดตตารางชื่อ "fuel_entries" (หากระบบของคุณใช้ชื่อตารางแบบมาตรฐานนี้)
ALTER TABLE IF EXISTS fuel_entries 
ADD COLUMN IF NOT EXISTS category text DEFAULT 'fuel';

ALTER TABLE IF EXISTS fuel_entries 
ADD COLUMN IF NOT EXISTS notes text DEFAULT '';

